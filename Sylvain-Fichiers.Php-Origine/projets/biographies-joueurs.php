<b>Introduction</b><br>

Les joueurs font le jeu. L'histoire du Jeu d'échecs est fascinante notamment de part les personnalités différentes des champions.

<br><br>

Nous pouvons constituer au moins 3 catégories de champions :<br>

- les champions internationaux du passé<br>

- les champions internationaux actuels<br>

- les champions français (passés et actuels)<br>

<br>

<b>Ressources</b><br>

En français, j'ai réalisé en 2014 ces deux cahiers avec les biographies des champions et championnes du monde, ainsi que pour chacun une sélection de 4 de leurs parties célèbres :<br>

- <a href="http://www.pseje.com/2014/FFE/DIFFE-DAFFE_2014_Cahier_Culture_3.pdf" target="_blank">les champions du monde</a> (PDF)<br>

- <a href="http://www.pseje.com/2014/FFE/DIFFE-DAFFE_2014_Cahier_Culture_4.pdf" target="_blank">les championnes du monde</a> (PDF)<br>

<br>

