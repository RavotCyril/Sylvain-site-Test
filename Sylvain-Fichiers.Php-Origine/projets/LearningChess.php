<strong>Présentation</strong><br />
<br />
<a href="https://learningchess.net/fr/" target="_blank">LearningChess</a> est une plateforme d'enseignement du jeu d'échecs en ligne pour les particuliers, les écoles et les clubs.<br />
<br />
<b>Ses points forts :</b> des leçons de qualité, une méthode pédagogique, une interface attractive, un entraînement tactique et un système de suivi de la progression des élèves.
<br><br>
Encore peu connu en France, puisque seulement partiellement traduit en français, LearningChess est la rencontre entre des entraîneurs hongrois reconnus : le grand maître <b>Jozsef Pinter</b> et le maître international Gabor Pirisi, et la société informatique i-Mobil Solutions. L'objectif a été d'allier du contenu échiquéen de qualité avec une plateforme en ligne qui le présente de manière attractive, interactive et ludique.<br />
<br />
L'une des grandes forces du site est de faire participer l'élève en lui posant des questions sous différentes formes : questions à choix multiple, coups à jouer sur l'échiquier, cases ou pièces sur lesquelles cliquer. Certaines corrections sont personnalisées selon la réponse donnée par l'élève. Chaque leçon, d'une durée de 30 à 45 minutes, se termine par un test de connaissances. La méthode d'enseignement étant principalement orientée vers les enfants, les aspects pédagogiques et ludiques ont été la préoccupation première (dessins, couleurs, animations, etc.).<br />
<br />
LearningChess a été conçu depuis le départ pour être utilisé en <b>milieu scolaire</b> ou dans un <b>club</b> d'échecs, en plus de l'usage personnel. C'est pourquoi les cours d'échecs contiennent 36 leçons, pour couvrir toute l'année scolaire à raison d'une leçon par semaine. De cette façon les trois niveaux de cours fournissent un programme sur trois ans, même s'il peut bien sûr être réalisé en moins de temps. L'enseignant (qu'il connaisse les échecs ou non...) peut préparer sa leçon en amont. Le site a également développé pour les enseignants un <a href="https://learningchess.net/fr/learning-management-guide" target="_blank">outil complet de suivi</a> de la <b>progression des élèves</b> via des tableaux de statistiques.<br />
<br />
Traduit en 8 langues, LearningChess compte actuellement plus de 150 000 utilisateurs dans le monde entier. La Fédération Tchèque des Échecs a décidé de le généraliser au niveau national, ainsi ce sont plus de 600 écoles et clubs qui l'utilisent dans tout le pays.<br />
<br />
Pour en savoir plus : <a href="https://learningchess.net/fr/help" target="_blank">la FAQ</a>.<br />
<br />
<strong>Version française</strong><br />
<br />
Une partie du site était déjà disponible en français mais une refonte a été réalisée durant l'été 2017, avec une reprise des textes existants et de nouvelles traductions. Elle est le fruit d'un partenariat entre LearningChess et l'entraîneur et Maître FIDE <a href="http://www.chess-teaching.com/contributeurs/sylvain_ravot.php" target="_blank">Sylvain Ravot</a>. Cette nouvelle version est officiellement annoncée le 1er septembre 2017.<br />
<br />
Les 36 leçons du premier niveau de cours (jusqu'à environ 1500 Elo), contenant plus de 1000 questions, sont <a href="https://learningchess.net/fr/unlock-home-use" target="_blank">accessibles</a> en français. Le deuxième niveau de cours (jusqu'à 1700 Elo) est prévu pour le 1er trimestre 2018 et le troisième niveau de cours (jusqu'à 1900 Elo) pour le dernier trimestre 2018.<br />
<br />
Chaque semaine à compter du 1er septembre, une nouvelle leçon du GM Jozsef Pinter sera <a href="https://learningchess.net/fr/grandmaster" target="_blank">mise en ligne</a> et accessible gratuitement.<br />
<br />
<a href="https://learningchess.net/fr/test" target="_blank">L'entraînement tactique</a> est également accessible gratuitement en français, avec des exercices choisis par les entraîneurs hongrois, en illimité, et l'exploration de différentes variantes d'une même position. La version payante permet de travailler par thème tactique.<br />
