<div style="text-align: justify;">
Le 1er septembre 2017,
<br><br>
Depuis plusieurs années, en observant les échecs français, je suis frappé de voir à quel point la majorité des acteurs échiquéens travaillent de leur côté, sans rien mettre en commun. Pas tous, mais force est de constater que l'approche collaborative, l'approche de partager et de mutualiser, est à ce jour minoritaire.
<br><br>
Ce serait sans doute moins dommageable si notre milieu échiquéen était très en avance, par rapport aux autres disciplines sportives par exemple, en terme de développement. Et si le volume financier qui y circulait était si grand que ses acteurs ne se sentent que peu concernés par cette question.
<br><br>
En me demandant pourquoi, j'ai esquissé deux raisons possibles :<br>
- chacun est très (trop ?) occupé à gérer son cas personnel, ce qui n'est déjà pas facile, notamment lorsqu'il faut payer les factures à la fin du mois et que l'on évolue dans une situation économique parfois précaire.<br>
- le manque de conscience collective, tendance que l'on retrouve simplement dans la société actuelle en général.
<br><br>
De manière paradoxale c'est un travail de mutualisation qui pourrait aider les individus à améliorer leur situation personnelle !
<br><br>
On peut aussi avancer un troisième point : la crainte que les autres nous "prennent" nos connaissances. J'ai l'impression que cette crainte se justifierait s'il y avait une concurrence réelle et directe entre les différents acteurs. Ce n'est pas le cas, en général soit cette concurrence n'existe pas, ou bien elle est assez indirecte. Ne serait-ce que géographiquement, étant donné la taille du territoire français. Qu'il s'agissent par exemple des animateurs, des entraîneurs ou encore des clubs. Au contraire c'est plutôt l'entretien de cette croyance de concurrence qui engendre des choses malsaines. On l'a vu trop souvent dans les rapports entre certains clubs par exemple. Avant de taper sur le club voisin, demande-toi plutôt si tu ne gagnerais pas à collaborer avec lui... Il y a plusieurs endroits où les clubs d'une même ville s'entraident et collaborent et ça engendre une belle réussite et une forte dynamique. Citons Nancy.<br>
Ce discours comporte une part utopiste, j'en suis conscient. Mais, comme on me l'a fait remarquer, cette part d'utopie représente aussi une force et un moteur.
<br><br>
En 2014 j'ai essayé de créer un élan pour une mutualisation, en <a href="http://www.chess-teaching.com/courrier_aux_entraineurs_francais.html" target="_blank">lançant un appel</a> aux entraîneurs. Quelques personnes m'ont répondu en disant qu'elles trouvaient que c'était une bonne idée. Mais ça n'a débouché sur rien de concret. Nous voilà trois ans plus tard. Je ne vais pas relancer un appel similaire, il est visiblement difficile de créer un élan collectif à partir de rien. Je vais donc procéder différemment. Je lance cette plateforme, à l'été 2017, et j'invite tous ceux qui le souhaitent à y contribuer, même modestement. Je la ferai évoluer dans tous les cas.
<br><br>
Si une personne vient partager ne serait-ce qu'un bon site, un bon outil, une bonne méthode, une réflexion, une bonne idée, et que tous ceux qui passent sur le site peuvent en prendre connaissance et potentiellement en profiter, le niveau collectif global augmente. C'est ce que l'on n'a pas actuellement et ce vers quoi on peine à tendre : une progression qualitative et significative de notre niveau général.
<br><br>
Cette plateforme a au moins deux objectifs :<br>
1) Débattre de ces questions, rechercher des moyens d'améliorer le développement des échecs en France<br>
2) Recenser l'existant, créer et réaliser des projets concrets, des collaborations, permettre aux acteurs qui le souhaitent de s'associer<br>
Le premier concernera davantage les différentes approches et philosophies, les partages d'expérience.<br>
Le deuxième est très concret et vise le recensement, la création et la réalisation.<br>
<br>
La plateforme elle-même en est au stade du projet et est destinée à évoluer pour prendre forme. N'hésitez pas à me contacter.
<br><br>
Salutations échiquéennes,
