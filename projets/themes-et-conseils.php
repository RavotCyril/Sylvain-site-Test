<a name="finales"></a>
<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">LES FINALES</div>

Même si toutes les parties ne se finissent pas en finales, c’est un secteur très important du jeu d’échecs (voir plus haut).
<br>

<p class="citation" style="width: 50%;">
<em>« 90% des variantes d’un livre d’ouverture n’ont pas de réelle utilité. En effet, soit elles contiennent des erreurs, soit elles sont basées sur des hypothèses fallacieuses. Oubliez donc simplement les ouvertures et consacrez votre temps à l’étude des finales.&nbsp;»</em><br>
<b>José Raúl Capablanca, 3<sup>e</sup> champion du monde</b>
</p>
Capablanca exagère et surtout il vivait à une autre époque. Il faut néanmoins tenir compte de l’importante part de vérité de son raisonnement. D’ailleurs il ne veut rien dire d’autres que Kérès.
<br><br>
L’étape nº1 est de savoir mater un Roi seul avec des pièces lourdes (Dame et Tour):<br>
- mat de l’escalier (avec Dame et Tour et avec deux Tours) | <a href="https://www.youtube.com/watch?v=BUUAQAWRbaE" target="_blank">Vidéo</a><br>
- mat avec la Dame (Roi et Dame contre Roi) | <a href="https://www.youtube.com/watch?v=krHMlZJJL1k" target="_blank">Vidéo</a><br>
- mat avec la Tour (Roi et Tour contre Roi)<br>
<br>
L’étape nº2 consiste à assimiler les positions théoriques fondamentales.<br>
Je vous conseille de commencer par la finale Roi et pion contre Roi. (en vidéo <a href="https://youtu.be/EfzL1gnCHIc" target="_blank">ici</a>)<br>
Puis par des finales de pions en général (sans aller trop loin dans la difficulté).<br>
Les finales de Tours les plus élémentaires.<br>
La finale Dame contre pion en 7ème rangée. (en vidéos <a href="https://youtu.be/1Q8Gtysu5n8?t=5562" target="_blank">ici</a> ou <a href="https://www.youtube.com/watch?v=ifGGlpjCKBE" target="_blank">là</a>)<br>
Quelques positions de finales de Fous et de Cavaliers.<br>
Les nulles théoriques fondamentales.<br>
<br>
L’étape nº3 est d’assimiler les grands principes de finales :<br>
- l’activité du Roi<br>
- la Tour en 7ème rangée<br>
- en avance matériel on échange les pièces mais pas les pions<br>
- le pion passé est fait pour être poussé<br>
- le pion candidat en avant<br>
- la Tour derrière le pion passé<br>
- les différents types de pions passés<br>
- ne pas se presser<br>
- les forteresses
<br><br>
Rejouez les finales de grands joueurs et analysez vos propres finales.
<br><br>
Je vous conseille d’acquérir au moins un livre de finales. Parmi les réputés : <em>La méthode Silman pour maîtriser les finales aux échecs</em> (Silman), <em>Entraînement aux finales</em> (Bernd Rosen). Un peu plus compliqués : <em>Fundamental Chess Ending</em> (Muller et Lamprecht) et <em>Les 100 finales qu’il faut connaître</em> (de la Villa).

