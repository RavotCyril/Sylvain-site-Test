<a name="ouvertures"></a>
<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">LES OUVERTURES</div>
Quel vaste sujet ! Qui fascine depuis toujours des générations de joueurs. Puis-je prendre un avantage sur mon adversaire dès le début de la partie ? Puis-je même pourquoi pas carrément le faire tomber dans un piège diabolique ?
<br><br>
Il est normal de se poser ces questions-là et la première est même totalement légitime. Mais je dois ici commencer par deux avertissements :<br>
1) Ne vous fixez pas comme objectif de faire tomber votre adversaire dans des pièges. A court terme cela vous fera gagner des parties, surtout contre des joueurs peu expérimentés, mais à long terme cela vous fait perdre du temps et peut vous éloigner d’une approche qui vous serait plus profitable à moyen et long terme, notamment au niveau de votre compréhension du jeu.<br>
Cela ne veut pas dire que vous ne pouvez pas retenir un piège ici ou là, en passant, mais ne jouez pas pour les placer à tout prix, votre objectif doit avant tout être de jouer des bons coups. Si un bon coup tend un piège, pas de souci, mais s’il faut jouer un coup douteux ou mauvais pour cela, c’est dommageable. Un bon exemple est le mat du berger. Si votre adversaire ne le connaît pas il peut se faire mater, mais s’il le connaît vous vous retrouvez en mauvaise position avec votre Dame sortie.<br>
J’irais même plus loin : même si votre piège fonctionne et vous fait gagner la partie dès le début, je trouve que c’est dommage : qu’avez-vous appris de cette partie ? Probablement rien. Comment allez-vous progresser dans ces conditions ?
<br>

<p class="citation" style="width: 43%;">
<em>« Votre seul travail dans les ouvertures est d’obtenir un milieu de partie jouable.&nbsp;»</em><br>
<b>Lajos Portish</b>
</p>

2) Les ouvertures sont un secteur très chronophage. C’est pour cette raison que j’ai parlé plus haut de la répartition de votre entraînement. Vous devez avoir à l’esprit que l’ouverture n’est qu’un secteur parmi les quatre secteurs du jeu. Si vous passez trop de temps à étudier vos ouvertures, ce sera au détriment de la tactique, de la stratégie et des finales.<br>
Le grand entraîneur Mark Dvoretsky écrivait qu’il entendait souvent des élèves qui lui disaient <em>qu’ils travailleront le reste quand ils auront fini leur répertoire d’ouvertures</em>. Il leur répondait <em>qu’on n’a jamais fini son répertoire</em> !<br>
Méfiez-vous, on peut vraiment se noyer dans ce secteur, travaillez-le avec parcimonie.
<br><br>
<b>Conseils :</b><br>
- essayez de travailler non pas seulement les coups d’ouvertures mais surtout <b>les idées</b> de ces coups<br>
- essayez de travailler vos ouvertures en fonction de leur structure de pions, c’est une approche qui vous fera travailler la stratégie, les plans, et qui vous servira potentiellement dans d’autres ouvertures (un excellent livre pour cela : <em>Les structures de pions aux échecs</em> de Flores Rios)<br>
- regardez des parties de tournois ayant été jouées avec votre ouverture (par exemple sur le site ChessTempo rubrique <a href="https://old.chesstempo.com/game-database.html" target="_blank">Base de données</a>). A ce sujet, je vous conseille de regarder les toutes premières parties jouées dans l’histoire avec votre ouverture, souvent des parties de la fin du 19e siècle ou début du 20e. Ces parties sont plus faciles à comprendre que les parties plus modernes. Cherchez également des parties de champions des différentes époques, et éventuellement des spécialistes (joueurs qui l’ont souvent utilisée).<br>
- cherchez un ou deux bons livres sur vos ouvertures principales, c’est sûrement encore à ce jour la meilleure façon de bien travailler une ouverture. Pour les autres vous pouvez chercher des vidéos qui expliquent (demandez-vous juste bien qui fait la vidéo, est-ce au moins un entraîneur expérimenté ?).
<br><br>

<b>À propos de cacher ses ouvertures</b><br>
C’est quelque chose que j’entends énormément. Je comprends tout à fait le désir de préserver quelque chose qu’on a passé du temps à travailler mais c’est au pire une illusion, et au mieux une mauvaise approche. J’en ai souvent parlé avec d’autres entraîneurs.<br>
Croyez-vous que la meilleure façon d’améliorer votre répertoire / votre ouverture soit de la laisser dans un placard ou de la tester au grand jour ? Bien sûr en la testant, et parfois en se prenant des coups auxquels on ne savait pas répondre. C’est en exposant vos ouvertures que vous les renforcerez, pas le contraire !<br>
Je vous recommande même l’approche opposée : si vous prévoyez un match d’entraînement avec un joueur, donnez-lui votre ouverture et dites-lui de se préparer contre ! Ce sera pour vous (et pour lui) beaucoup plus intéressant et instructif que s’il ne savait pas quoi jouer dessus. Chacun améliore ainsi son répertoire, ses connaissances et son expérience.<br>
(tellement plus qu’en jouant 1.g3 pour cacher son ouverture incroyable ;-) )

<br><br>
<b>Construire son répertoire</b><br>
La construction de son répertoire est source de nombreuses questions : comment mémoriser, comment choisir ses ouvertures, devrais-je jouer plutôt des grandes variantes ou des sous-variantes, comment travailler une ouverture, devrais-je apprendre un répertoire ou plusieurs, quand aurai-je terminé mon répertoire, etc.<br>
Voici quelques sources pouvant vous aider à y répondre :<br>
- mon PDF de 4 pages <a href="docs/Comment_construire_son_repertoire.pdf">Comment construire son répertoire</a><br>
- le chapitre <a href="https://youtu.be/QE0HKjxkS_8?t=5265" target="_blank">Construire son répertoire</a> de la masterclass FFE avec le MI Xavier Bédouin<br>
- une vidéo de JPG Chess <a href="https://youtu.be/djVbhJQ6YrI" target="_blank">"Comment faire son répertoire"</a>

<br><br>
<a name="attaque"></a>
<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">LES MÉTHODES D’ATTAQUE SUR LE ROI</div>
On parle souvent des tableaux de mat et des thèmes tactiques, moins souvent des méthodes typiques d’attaque sur le Roi, mais il ne faut pas les oublier.<br>
Deux livres (parmi d’autres !) qui me viennent en tête à ce sujet : <em>L’art de l’attaque</em> de Vukovic et <em>L’école des échecs</em> le tome 2 de Kotov.<br>
Voici quelques méthodes :<br>
- attaquer le Roi au centre (case f7, diagonale h5-e8 / a4-e8, colonnes centrales)<br>
- sacrifice Fxh7+ (Calabrais ou cadeau grec)<br>
- double sacrifice de Fous (Lasker-Bauer, Nimzowitsch-Tarrasch, etc.)<br>
- attaque sur h7<br>
- attaque sur g7<br>
- ouverture des lignes (colonnes, diagonales, levier de pion)<br>
- l’assaut de pions<br>
- la Tour qui coulisse horizontalement<br>
- attaquer un fianchetto<br>
- attaquer un grand roque<br>
- l’extraction du Roi<br>
- les conditions pour attaquer / sacrifier

<br><br>
<a name="ordinateur"></a>
<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">L’ORDINATEUR</div>
Les modules d’analyse, comme Stockfish, sont un outil extraordinaire mais le joueur qui cherche à progresser doit veiller à ne pas lui faire aveuglément confiance et s’en retrouver esclave :)<br>

<p class="citation" style="width: 45%;">
<em>« Mais qu’importe ce que vous direz et ce que je dirai, une machine pouvant jouer aux échecs avec autrui est l’une des plus belles merveilles de notre 20e siècle !&nbsp;»</em><br>
<b>David Bronstein, vice-champion du monde 1951</b>
</p>

- L’ordinateur est hyper fort (voire « trop fort »), mais n’oublions pas qu’il est incapable d’expliquer ni ses évaluations, ni ses coups (il existe des projets avec tentatives de générations de d’explications mais pour l’instant en tout cas cela ne concerne pas les modules utilisés sur les plateformes classiques).<br>
- Pour progresser aux échecs, votre cerveau doit s’habituer à reconnaître les bons et les mauvais schémas de lui-même, il doit s’habituer à produire les raisonnements échiquéens, cela demande du <b>temps</b> et de <b>l’autonomie</b> : soit tout le contraire de ce qu’on obtient quand on regarde ce que propose l’ordinateur puisqu’il donne la « réponse » 1) instantanément et 2) c’est lui qui travaille, pas notre cerveau.<br>
<br>
D’ailleurs au passage je rappelle que Magnus Carlsen, le champion du monde actuel, a très peu utilisé l’ordinateur au cours de sa formation échiquéenne et on pourrait dire la même chose de la majorité des super GM actuels.
<br><br>
Quelques conseils :<br>
- méfiez-vous de ce qu’il dit dans l’ouverture, quand vous le pouvez utilisez plutôt les bibliothèques d’ouvertures, qui contiennent les coups joués par des humains dans les tournois (un coup qui a été joué des milliers de fois par des très bons joueurs est 10 fois plus digne de votre confiance)<br>
- méfiez-vous de son évaluation, qui de nos jours peuvent monter très haut dans des positions encore peu claires pour un oeil humain, et qui peuvent laisser croire que c’est "facilement" gagnant<br>
- méfiez-vous de certaines évaluations en finales, étant donné qu’elle n’intègre quasiment pas les positions de nulle théoriques ni la notion de forteresse (utilisez les tables de finales si possible)<br>
- méfiez-vous des coups proposés par l’ordinateur, lui calcule des millions de coups par seconde et peut se permettre des choses que des humains ne devraient pas faire. Faites-lui surtout confiance quand il s’agit de coups tactiques clairs (attaque de mat, gain de matériel) et utilisez votre esprit critique en relation avec les principes échiquéens<br>
- désactivez l’ordinateur pour analyser et habituer votre cerveau à réfléchir par lui-même, puis <b>comparez</b> le fruit de vos analyses avec ce que propose l’ordinateur<br>
<br>
