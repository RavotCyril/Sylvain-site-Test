<a name="repartir"></a>
<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">RÉPARTIR SON ENTRAÎNEMENT</div>

Jusqu’à 2000 elo, la majorité des parties se décident sur des coups tactiques. C’est donc le secteur auquel vous devriez accorder le plus d’importance dans votre entraînement.<br>
Exemple de répartition d’entraînement pour un joueur qui débute :<br>
- Ouvertures 10&nbsp;%<br>
- Tactique 50&nbsp;%<br>
- Stratégie 25&nbsp;%<br>
- Finales 15&nbsp;%<br>
<br>
Remarques :<br>
- on peut mal commencer une partie mais se rétablir en milieu de partie ou fin de partie, tandis que si on se trompe dans la finale, il y a nettement plus de chances que cela nous coûte la partie. C’est pourquoi je pense que vous devriez dédier plus de temps aux finales. L’avantage est que ce que vous apprenez en finales ne changera pas dans le temps et vous servira toujours, ce qui vous permettra ensuite de dédier plus de temps aux autres secteurs, notamment la stratégie et les ouvertures.
<br>
<p class="citation" style="width: 50%;">
<em>« Un joueur peut parfois s’offrir le luxe de commettre une imprécision, voire une erreur pendant l’ouverture ou le milieu de jeu, sans nécessairement perdre la position. Dans une finale… une erreur peut être décisive et les secondes chances se présentent rarement.&nbsp;»</em><br>
<b>Paul Keres</b>
</p>
- quand vous aurez beaucoup travaillé la tactique, vu des milliers de schémas/motifs/situations tactiques, vous pourrez là aussi réduire la part de tactique dans votre entraînement, au profit des 3 autres secteurs.
<br>
- il est possible de travailler le secteur des ouvertures en faisant un lien avec le milieu de jeu, en étudiant les plans correspondants et des parties de tournoi. Dans ce cas il y a un travail à la fois d’ouvertures et de stratégie.
<br>
- notez que quand vous analysez vos parties, vous travaillez potentiellement sur les 4 secteurs du jeu.
<br><br>
Exemple de répartition d’entraînement pour un joueur avec 2 ans d’échecs :<br>
- Ouvertures 20%<br>
- Tactique 30%<br>
- Stratégie 30%<br>
- Finales 20%<br>
<p class="citation" style="width: 35%;">
<em>« Pour me battre, il faut me battre trois fois : une fois dans l’ouverture, une fois dans le milieu de jeu, et une fois en finale.&nbsp;»</em><br>
<b>Alexandre Alekhine, 4<sup>e</sup> champion du monde</b>
</p>

