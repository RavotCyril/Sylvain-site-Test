<a name="psychologie"></a>
<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">LA PSYCHOLOGIE</div>
Le jeu d’Echecs - et c’est heureux - n’est pas constitué que de technique, c’est aussi un jeu très psychologique. C’est ce qu’avait compris Emanuel Lasker avant tout le monde.<br>

<p class="citation" style="width: 55%;">
<em>« Les Échecs mettent en conflit, non pas deux intelligences, mais deux volontés.&nbsp;»</em><br>
<b>Emanuel Lasker, 2<sup>e</sup> champion du monde</b>
</p>

<p class="citation" style="width: 55%;">
<em>« Les Échecs sont 30 à 40% psychologiques. Cette dimension disparaît quand on affronte un ordinateur.&nbsp;»</em><br>
<b>Judith Polgar, plus forte joueuse de l’histoire</b>
</p>

