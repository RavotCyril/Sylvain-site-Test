<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">LES MINIATURES</div>
Une partie miniature est une partie qui se termine en moins de 20 coups.<br>
Les étudier est un moyen intéressant de travailler les thèmes Avance de développement, Sécurité du Roi, Ouverture des lignes, en même temps que des motifs tactiques, tout en voyant différentes ouvertures.<br>
