<a name="strategie"></a>
<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">LA STRATÉGIE</div>
La stratégie occupe une place très importante, même quand les joueurs ne sont pas encore très à l’aise tactiquement. En premier lieu en terme de compréhension de la sécurité du Roi (critère nº1 comme vu plus haut), ensuite et surtout en terme d’activité des pièces, de savoir quelles pièces échanger/garder, d’avoir des idées de comment manier ses pions (l’âme du jeu d’échecs selon Philidor), où jouer (aile-Dame / centre / aile-Roi), s’il faut agir vite ou prendre son temps, identifier des cibles et des plans, etc.<br>

<p class="citation" style="width: 37%;">
<em>« La tactique, c’est ce que vous faites quand il y a quelque chose à faire ; la stratégie, c’est ce que vous faites quand il n’y a rien à faire.&nbsp;»</em><br>
<b>Xavier Tartacover </b>
</p>

<b>Principaux thèmes stratégiques :</b><br>
- la sécurité du Roi<br>
- l’activité des pièces (incluant bon/mauvais Fou)<br>
- les échanges<br>
- l’espace<br>
- la case forte/faible<br>
- la colonne ouverte<br>
- l’avance de développement<br>
- les roques opposés<br>
- l’évaluation d’une position<br>
- le centre de pions<br>
- l’ouverture des lignes, le levier<br>
- la réaction centrale<br>
- l’initiative<br>
- la 7ème rangée (<a href="https://youtu.be/CuobEa0qK1k" target="_blank">vidéo</a>)<br>
- la centralisation<br>
- la prophylaxie<br>
- les majorités de pions<br>
- l’avant-poste (et ses différentes définitions)<br>
- la paire de Fous<br>
- le pion arriéré<br>
- les pions faibles<br>
- la faiblesse sur une couleur<br>
- les attaques de Fous de couleurs opposées (<a href="https://www.youtube.com/watch?v=y_U5YzhfBaw" target="_blank">vidéo</a>)<br>
- le principe des deux faiblesses<br>
- le pion isolé central<br>
- l’attaque de minorité<br>
- le pion passé (utilisation et blocage)<br>
<br>
La stratégie est tout autant au coeur du jeu que la tactique.
<br><br>
La page de ChessTempo <a href="https://chesstempo.com/positional-motifs" target="_blank">Thèmes Stratégiques</a>
