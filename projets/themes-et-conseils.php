<a name="reperes"></a>
<div style="font-size: 22px; color: blue; font-weight: bold; margin-bottom: 5px;">QUELQUES POINTS DE REPÈRE</div>
<b>Secteurs du jeu :</b><br>
- Ouvertures (débuts de partie)<br>
- Tactique (calcul, combinaisons)<br>
- Stratégie (organisation, placement, planification)<br>
- Finales (fins de parties)<br>
<br>
<b>Phases de progression d’un joueur :</b><br>
1) Phase matérielle (être concentré sur le matériel, le déplacement des pièces et les captures)<br>
2) Phase spatiale (prendre conscience des cases, du contrôle des cases, d’éléments géométriques)<br>
3) Phase temporelle (prendre conscience des gains/pertes de temps, quand faire quoi, quand agir vite ou non)<br>

<p class="citation" style="width: 45%;">
<em>« Les échecs ont trois composantes : le temps, l’espace et le matériel en dernier.&nbsp;»</em><br>
<b>Mikhail Tal, 8<sup>e</sup> champion du monde</b>
</p>

<br>
<b>Critères d’évaluation d’une position (par ordre d’importance) :</b><br>
1) Sécurité du Roi (le mat met fin à la partie)<br>
2) Matériel (un surnombre aide à gagner)<br>
3) Activité des pièces (quand les joueurs font assez attention aux critères 1 et 2, cela sera souvent décisif)<br>
4) Structure de pions (et ses conséquences, en particulier les pions faibles)<br>
<br>
<b>Recommandations importantes pour progresser :</b><br>
- Jouez, jouez et jouez<br>
- Analysez vos parties (seul, sans l’ordinateur puis avec, et si possible avec un entraîneur ou un joueur expérimenté)<br>
- Multipliez les exercices de calcul (tactiques principalement, cela peut aussi être dans des finales et des <a href="http://www.chess-teaching.com/projets/biographies-compositeurs.php" target="_blank">études</a>)<br>
- Regardez des parties (en direct, commentées, etc.)<br>
<br>
Mon document <a href="docs/Analyser_ses_parties.pdf">Comment analyser ses parties</a>
<br><br>
<b>Conseils pour jouer sur internet :</b><br>
- préférez jouer avec un incrément, qui donne la prime au jeu sur la pendule<br>
- utilisez une cadence d’au moins 10 minutes, si possible 15 voire plus. Il est difficile de construire une vraie partie d’échecs avec moins de temps<br>
- il est bon de jouer beaucoup mais essayez de privilégier la qualité sur la quantité : mieux vaut moins de parties mais mieux soignées et avec des cadences plus longues, que vous analyserez pour en retirer des enseignements<br>
<br>
Mon document <a href="docs/Jouer_sur_Internet.pdf">Jouer sur Internet</a>
