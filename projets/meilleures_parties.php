<font size=5>Les meilleures parties d'échecs</font>
</div>
<br>
Ce projet se propose de répertorier les meilleures parties d'échecs de l'histoire, historiques ou récentes.<br>
Le terme <i>"meilleure"</i> étant subjectif, j'y reviendrai ci-dessous.
<br><br>
Il existe de nombreux livres, articles, vidéos, listant les meilleures parties de l'histoire des échecs. Je m'inspire du livre au long titre <i>"The mammoth book of the world's greatest chess games"</i>, préfacé par Anand, coécrit par les auteurs anglais Burgess, Nunn et Emms. Ils ont sélectionné 112 parties, de 1834 (McDonnell-Labourdonnais) à 2002 (Sutovsky-Smirin). Ils avaient auparavant établi une short-list de 230 parties. Chacun leur a attribué une note entre 1 et 5. Seulement 3 parties ont atteint le score maximum de 15 : <a href="http://www.chessgames.com/perl/chessgame?gid=1031957" target="_blank">Botvinnik-Capablanca 1938</a>, <a href="http://www.chessgames.com/perl/chessgame?gid=1067175" target="_blank">Karpov-Kasparov 1985 (16)</a>, <a href="http://www.chessgames.com/perl/chessgame?gid=1011478" target="_blank">Kasparov-Topalov 1999</a>.<br />
<br />
Je reviens donc sur les critères de "meilleure partie", m'inspirant des leurs :<br />
- un véritable combat<br />
- qualité de jeu, non seulement du gagnant mais aussi du perdant<br />
- jeu inventif / novateur / brillant<br />
<br />
Deux critères peuvent se rajouter :<br />
- valeur pédagogique<br />
- importance historique
<br><br>
L'intérêt est décuplé si la partie est bien commentée. Si possible listons au choix :<br>
- la référence d'un livre où elle est commentée<br>
- un lien internet où elle est commentée<br>
- un lien vers une vidéo où elle est commentée<br>
<br>
Des votes par internet pourraient être une idée afin d'établir des classements, par exemple par période.<br>
Enfin il y a une passerelle vers le projet <a href="http://chess-teaching.com/projets/biographies-joueurs.php" target="_blank">biographies de joueurs</a> puisque c'est un plus si elles sont assorties d'une sélection de leurs meilleures parties.
